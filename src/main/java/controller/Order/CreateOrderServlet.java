    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        HttpSession s = request.getSession();
        String a = ((Account) s.getAttribute("acc")).getUserName();
        request.setAttribute("usern", a);
        request.getRequestDispatcher("Order.jsp").forward(request, response);
    }

    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        String id = request.getParameter("orderID");
        String usern = request.getParameter("userName");
        double total = Double.parseDouble(request.getParameter("orderTotal"));

        String des = request.getParameter("orderDes");
        String date = request.getParameter("orderDate");
        
        Order o = new Order(id, usern, total, date, des);
        OrderDAO odao = new OrderDAO();
        odao.addNew(o);
        response.sendRedirect("listOrder");

    }

